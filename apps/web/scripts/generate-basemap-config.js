import fs from "node:fs";
import path from "node:path";

// Build-time basemap configuration generator.
//
// Reads PUBLIC_BASEMAP_MODE and writes src/lib/generated/basemapConfig.ts.
// The remote (CARTO) style URL is emitted ONLY for an explicit remote-style
// build. For local-sovereign (and the unset default) the generated module
// contains no remote URL at all, so the client bundle is guaranteed
// CARTO-free regardless of the bundler's dead-code elimination.
//
// The basemap mode policy (allowed modes, default) is defined in
// basemap-mode.policy.json. This generator enforces that policy and emits
// the CARTO URL only for remote-style mode.

const REMOTE_STYLE_URL =
  "https://basemaps.cartocdn.com/gl/voyager-gl-style/style.json";

// Load the basemap mode policy.
const policyPath = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  "..",
  "basemap-mode.policy.json",
);
const policy = JSON.parse(fs.readFileSync(policyPath, "utf8"));

const rawMode = process.env.PUBLIC_BASEMAP_MODE;

let mode;
if (rawMode === undefined || rawMode === "") {
  mode = policy.defaultMode;
} else if (policy.allowedModes.includes(rawMode)) {
  mode = rawMode;
} else {
  console.error(`ERROR: Invalid PUBLIC_BASEMAP_MODE='${rawMode}'.`);
  console.error(
    `       Allowed values: ${policy.allowedModes.join(", ")} (or unset for default: ${policy.defaultMode}).`,
  );
  process.exit(1);
}

const header = `// AUTO-GENERATED by scripts/generate-basemap-config.js — do not edit.
// Build-time basemap configuration. In local-sovereign mode this file contains
// no remote (CARTO) URL, which is what guarantees a CARTO-free client bundle.

export type BuildBasemapConfig =
  | { mode: "local-sovereign" }
  | { mode: "remote-style"; styleUrl: string };
`;

const body =
  mode === "remote-style"
    ? `
export const BUILD_BASEMAP_CONFIG: BuildBasemapConfig = {
  mode: "remote-style",
  styleUrl: "${REMOTE_STYLE_URL}",
};
`
    : `
export const BUILD_BASEMAP_CONFIG: BuildBasemapConfig = {
  mode: "local-sovereign",
};
`;

const outDir = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  "..",
  "src/lib/generated",
);
const outFile = path.join(outDir, "basemapConfig.ts");

if (!fs.existsSync(outDir)) {
  fs.mkdirSync(outDir, { recursive: true });
}

fs.writeFileSync(outFile, header + body, "utf8");

console.log(`Generated basemap config: mode=${mode} at ${outFile}`);
